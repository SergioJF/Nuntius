package com.nuntius.webservice;

public class Comment {

	private Integer id;
	
	private String text;
	
	

	public Comment() {
		super();

	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
